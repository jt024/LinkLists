    private void update(Link next)
    {
        

        
        if (numStack.getHead()==null && numStack.getHead()==null) {
            numStack = new Stack();
            taLinks.setText("");
        }
        else{
            taLinks.setText("");
            numStack.resetValueString();
            taLinks.setText(numStack.buildValueString(next));            
        }
        
        textFieldInteger.requestFocusInWindow();
        textFieldInteger.setText("");

    }
    

